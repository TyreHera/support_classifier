import pandas as pd
import random

# Шаблоны фраз для разных категорий
templates = {
    "Оплата": [
        "Не проходит оплата картой", "Дважды списали деньги за заказ", "Как вернуть средства?", 
        "Ошибка при вводе CVV", "Не работает Apple Pay", "Где мой чек?", "Терминал выдал ошибку"
    ],
    "Техническая ошибка": [
        "Приложение вылетает при запуске", "Не могу войти в личный кабинет", "Сайт выдает ошибку 500", 
        "Бесконечная загрузка на странице профиля", "Не приходят пуш-уведомления", "Забыл пароль и не приходит код"
    ],
    "Доставка": [
        "Где мой курьер?", "Заказ задерживается уже на 3 часа", "Привезли не по тому адресу", 
        "Можно ли изменить время доставки?", "Статус заказа не обновляется", "Посылка повреждена"
    ],
    "Спам/Прочее": [
        "У вас отличный сервис!", "Как дела?", "Предлагаем услуги по продвижению сайта", 
        "Куплю гараж", "Перейдите по ссылке чтобы получить миллион", "Спасибо, всё круто"
    ]
}

# Назначение приоритетов
priorities = {
    "Оплата": ["Высокий", "Средний"],
    "Техническая ошибка": ["Высокий", "Средний"],
    "Доставка": ["Средний", "Низкий"],
    "Спам/Прочее": ["Низкий"]
}

data = []
# Генерируем 3000 строк
for _ in range(3000):
    category = random.choice(list(templates.keys()))
    text = random.choice(templates[category])
    # Немного модифицируем текст, чтобы добавить разнообразия
    text = text + " " + random.choice(["Помогите!", "Срочно.", "Что делать?", "", "Жду ответа."])
    priority = random.choice(priorities[category])
    data.append({"text": text.strip(), "category": category, "priority": priority})

df = pd.DataFrame(data)
df.to_csv("dataset.csv", index=False, encoding="utf-8")
print("Датасет dataset.csv успешно создан!")